/**
 * Returns an SVG string for the given ECF category.
 * @param {string|null} category - e.g. "Weapons/Items", "Medical/Items"
 * @returns {string} SVG markup
 */
export function getCategoryIcon(category) {
  const key = (category ?? '').toLowerCase().split('/')[0].trim();
  return ICON_MAP[key] ?? DEFAULT_ICON;
}
